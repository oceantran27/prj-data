#include <set>
#include <unordered_map>
#include <algorithm>
using namespace std;

class Solution {
public:
    void inorder(TreeNode* root, vector<int>& res) { // 树的中序遍历
        if (!root) {
            return;
        }
        inorder(root->left, res);
        res.push_back(root->val);
        inorder(root->right, res);
    }
    vector<int> inorderTraversal(TreeNode* root) {
        vector<int> res;
        inorder(root, res);
        return res;
    }
};


a=int(input("Enter First Number:"))
b=int(input("Enter Second Number:"))
c=input("Enter The sign:")
if c=='*':
    print("Multiplication:",a*b)
if c=='+':
    print("Addition:",a+b)
if c=='-':
    print("Substraction:",a-b)
if c=='/':
    print("Division:",a/b)
if c=='%':
    print("Moduls:",a%b)
if c=='**':
    print("Power:",a**b)
if c=='//':
    print("Floor Division:",a//b)

#include <bits/stdc++.h>
#include <vector>
#include <iostream>
#include <string>
#include <map>
#include <set>
#include <unordered_map>
#include <algorithm>
using namespace std;

// Time Complexity: O(n)
// Space Complexity: O(1)

class Solution {
    public:
        int canCompleteCircuit(vector<int> &gas, vector<int> &cost) {
            int total = 0;
            int sum = 0;
            int j = -1;

            for(int i = 0; i < gas.size(); ++i) {
                total += gas[i] - cost[i];
                sum += gas[i] - cost[i];
                if(sum < 0) { // find the gas station which should be the last one
                    sum = 0;
                    j = i;
                }
            }

            return (total >= 0) ? j + 1 : -1;
        }
};
